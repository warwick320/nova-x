size_t module::andriod::build_adv(uint8_t* buf) {
    uint32_t model = models[esp_random() % std::size(models)];

    uint8_t i = 0;

    buf[i++] = 3;
    buf[i++] = 0x03;
    buf[i++] = 0x2C;
    buf[i++] = 0xFE;

    buf[i++] = 6;
    buf[i++] = 0x16;
    buf[i++] = 0x2C;
    buf[i++] = 0xFE;
    buf[i++] = (model >> 16) & 0xFF;
    buf[i++] = (model >>  8) & 0xFF;
    buf[i++] = model & 0xFF;

    buf[i++] = 2;
    buf[i++] = 0x0A;
    buf[i++] = static_cast<uint8_t>((esp_random() % 120) - 100);

    return i;
}
